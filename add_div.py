#Write a program for addition and division for two numbers

#Addition
num1 = float(input("Enter the first number for addition :"))
num2 = float(input("Enter the second number for addition :"))
sum = num1 + num2
print(f"The sum of {num1} and {num2} is {sum}")

#Division
num3 = float(input("Enter the third number for division :"))
num4 = float(input("Enter the fourth number for division :"))
div = num3 / num4
print(f"The division of {num3} and {num4} is {div}")