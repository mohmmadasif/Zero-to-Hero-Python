#Write a program to swap two numbers


#Input for variables
a = float(input("Enter the value of number 1 "))
b = float(input("Enter the value of number 2 "))


#Swap of the variables
c = a
a = b
b = c


#Output for variables
print(f"The swap value of number 1 is {a} and swap value of number 2 is {b}")